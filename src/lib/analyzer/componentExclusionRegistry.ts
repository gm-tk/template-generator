/**
 * Returns true if ANY class in the provided list matches the Component Exclusion Registry.
 *
 * Matching rules:
 * - Case-sensitive: 'TKmodal' matches, 'tkmodal' does not.
 * - Token-based: Match whole class tokens only. 'carousel' matches as a token,
 *   but 'carousel-caption' is a different token and does not match.
 */
export function isExcludedComponent(classList: string[]): boolean {
  return classList.some((cls) => COMPONENT_EXCLUSION_REGISTRY.has(cls));
}
